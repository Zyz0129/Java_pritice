package FinalDemo;

public class FinalDemo1 {
    public static void main(String[] args) {
        /*
            final修饰方法：
                    表明该方法是最终方法，不可被重写
            final修饰类：
                    表明该类是最终类，不可被继承
            final修饰变量
                    表为常量，不可被修改
         */
        String s;
        s="120";
        System.out.println(s);
    }
}
